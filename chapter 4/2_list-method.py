# 1 sort method
a = [22,11,143,43,10,5,99]

a.sort()

print(a)

# 2 reverse method
b = [22,11,143,43,10,5,99]

b.reverse()

print(b)

# 3 append method
c = [22,11,143,43,10,5,99]

c.append(143)

print(c)

# 4 insert method
d = [22,11,143,43,10,5,99]

d.insert(2, 99)

print(d)

# 5 remove method
e = [22,11,143,43,10,5,99]

e.remove(43)

print(e)

# 6 pop method
f = [22,11,143,43,10,5,99]

f.pop(1)

print(f)